int	ft_iterative_factorial(int nb)
{
	int	n;

	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	if (nb <= 2)
		return (nb);
	n = nb;
	while (1 < --nb)
		n = n * nb;
	return (n);
}
